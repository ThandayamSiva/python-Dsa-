# intialize a empty array []

array = []
print(array)

# append element using built in method
arr = [1,2,3,4,5,6]
arr.append(7)
print(arr)

# Update element using index

arr[6] = 8
print(arr)
 
# access element using Index
find = arr[3]
print(find)

# delete element in array
del arr[1]
print(arr)

# pop()
arr.pop()
print(arr)

# remove(value)
arr.remove(4)  
print(arr)

